package ATB_XM_Test_Programs;

public class Increment_Diff {
    public static void main(String[] args) {
        int i=10;
        System.out.println("++i is"+ ++i);
        System.out.println("i value is " + i);

        int a_post= 10;
        System.out.println("a_post++ is "+ a_post++);
        System.out.println("a_post value is "+ a_post);

        }
}
